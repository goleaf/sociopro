import { readFileSync } from 'node:fs';

const messagePath = process.argv[2];

if (!messagePath) {
    process.exit(0);
}

const message = readFileSync(messagePath, 'utf8').trim();
const firstLine = message.split('\n')[0] || '';
const conventionalPattern = /^(feat|fix|docs|test|refactor|perf|style|build|ci|chore|revert)(\([a-z0-9-]+\))?!?: [a-z0-9].{2,86}$/;
const blockers = [];

if (!conventionalPattern.test(firstLine)) {
    blockers.push('Use Conventional Commits: <type>[optional scope]: <imperative summary>.');
}

if (/[.]$/.test(firstLine)) {
    blockers.push('Do not end the commit subject with a period.');
}

if (/\b(ai|codex|claude|generated by)\b/i.test(message)) {
    blockers.push('Do not mention agent internals unless that is the product change.');
}

if (blockers.length === 0) {
    process.exit(0);
}

console.error([
    'Sociopro commit-msg guard blocked this commit:',
    `- Subject: ${firstLine || '(empty)'}`,
    ...blockers.map((blocker) => `- ${blocker}`),
].join('\n'));

process.exit(1);
